using Test

function test_column_values(matrix)
    for j in 1:size(matrix, 2)
        unique_values = unique(matrix[:, j])
        @test length(unique_values) == 2
        for value in unique_values
            count = sum(matrix[:, j] .== value)
            @test count == 5
        end
    end
end

